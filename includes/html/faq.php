	<h4>Q: How do I request for a Snug meet?</h4>
	<p>A: All of our members can be found on our website and our apps – pick your choice, pay the request fee, select a café or a virtual meet and you’re good to go meet them at the café / online meet within the stipulated time, once the member has accepted your request.</p>


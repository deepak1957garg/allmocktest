	<h2>Receiving a yes for a Snug meet</h2><br/>
	<h4>Q: How will I receive my Snug meet acceptance once the member agrees?</h4>
	<p>A: It will be sent to you as an app notification to the phone number that you provided when you requested the Snug meet. Alternatively, we will also send you a WhatsApp notification and an email.</p>


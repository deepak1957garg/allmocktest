	<h2>About Snug</h2><br/>
	<h4>Q: What is Snug?</h4>
	<p>A: Snug is a platform where one can request to meet other diverse human beings. Snug believes that, every human can enrich your life through their life experiences, through their stories, through their talents. We are a community of equals. You can request the time of another member, at a nominal request fee for a coffee at a nearby café or a video meet. The meet is face to face one on one. Our mission is to get you to experience people and create the most enriching, authentic and memorable real life experiences for people in the real world or the virtual one.</p>

	<h4 id="faq3">Q: Why should I meet Snug diverse people?</h4>
	<p>A: It is important to acknowledge and recognise that meeting new people, especially those different from yourself, is an opportunity to grow as an individual.</p>
	<p>We all have our own cultural beliefs based on the religious beliefs, sense of humor, customs, and views toward family, work, health, & social issues passed down to us by our family, community, and the media. When we step outside of this echo chamber we are given the chance to strengthen, challenge or change these. </p>
	<p>Even when encountering people with values we may not agree with we are given the opportunity to see the world from another’s viewpoint. This opportunity allows for inner growth on a wider, human  level as it fosters greater understanding and empathy for our fellow humans. It may not change how we feel about an issue, but it gives us the chance to understand all of the nuances and who else this affects.</p>
	<p>Without contact with people outside our comfort zone, it is easy to fall victim to stereotypical thoughts and misconceptions. Until real life experiences are had with others we cannot really tap into our innate human capabilities.</p>

